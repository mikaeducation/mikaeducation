<script>
    const newsItems = document.querySelectorAll('.news-item');

    newsItems.forEach(item => {
        item.addEventListener('click', function() {
            // buat reset semua item ke tampilan awal
            newsItems.forEach(news => {
                news.classList.remove('active-news', 'min-w-[425px]', 'h-[550px]', 'gradient-news');
                news.classList.add('min-w-[325px]', 'h-[450px]');
                news.querySelector('.baca-link')?.classList.add('hidden');
            });
            
            // buat aktifkan item yang diklik
            this.classList.add('active-news', 'min-w-[425px]', 'h-[550px]', 'gradient-news');
            this.classList.remove('min-w-[325px]', 'h-[450px]');
            this.querySelector('.baca-link').classList.remove('hidden');
        });
    });

    const newsContainer = document.getElementById('newsContainer');
    let isDown = false;
    let startX;
    let scrollLeft;

    newsContainer.addEventListener('mousedown', (e) => {
        isDown = true;
        startX = e.pageX - newsContainer.offsetLeft;
        scrollLeft = newsContainer.scrollLeft;
    });

    newsContainer.addEventListener('mouseleave', () => {
        isDown = false;
    });

    newsContainer.addEventListener('mouseup', () => {
        isDown = false;
    });

    newsContainer.addEventListener('mousemove', (e) => {
        if (!isDown) return;
        e.preventDefault();
        const x = e.pageX - newsContainer.offsetLeft;
        const walk = (x - startX) * 2;
        newsContainer.scrollLeft = scrollLeft - walk;
    });

    document.addEventListener("DOMContentLoaded", function () {
        setTimeout(() => {
            const container = document.getElementById("newsContainer");
            if (container) {
                container.scrollLeft = (container.scrollWidth - container.clientWidth) / 2;
            }
        }, 100);
    });
</script>
