<script>
    document.addEventListener("DOMContentLoaded", function () {
    const menuToggle = document.getElementById("menu-toggle");
    const dropdownMenu = document.getElementById("dropdown-menu");
    const aksesibilitasBtn = document.getElementById("aksesibilitas-btn");
    const dropdownAksesibilitas = document.getElementById("dropdown-aksesibilitas");

    // Tambahkan overlay saat dropdown dibuka
    const overlay = document.createElement("div");
    overlay.classList.add("fixed", "inset-0", "bg-black", "bg-opacity-50", "hidden", "z-30");
    document.body.appendChild(overlay);

    // Tampilkan/hilangkan dropdown pertama saat ikon diklik
    menuToggle.addEventListener("click", function (event) {
        event.stopPropagation();
        const isHidden = dropdownMenu.classList.contains("hidden");

        dropdownMenu.classList.toggle("hidden");
        dropdownAksesibilitas.classList.add("hidden");

        // Tampilkan overlay hanya jika dropdown dibuka
        if (isHidden) {
            overlay.classList.remove("hidden");
        } else {
            overlay.classList.add("hidden");
        }
    });

    // Tampilkan dropdown Aksesibilitas dan sembunyikan dropdown utama
    aksesibilitasBtn.addEventListener("click", function (event) {
        event.stopPropagation();
        dropdownMenu.classList.add("hidden");
        dropdownAksesibilitas.classList.toggle("hidden");
    });

    // Sembunyikan dropdown dan overlay jika klik di luar menu
    document.addEventListener("click", function () {
        dropdownMenu.classList.add("hidden");
        dropdownAksesibilitas.classList.add("hidden");
        overlay.classList.add("hidden");
    });

    // Mencegah dropdown aksesibilitas hilang saat diklik di dalamnya
    dropdownAksesibilitas.addEventListener("click", function (event) {
        event.stopPropagation();
    });

    // Menutup dropdown jika overlay diklik
    overlay.addEventListener("click", function () {
        dropdownMenu.classList.add("hidden");
        dropdownAksesibilitas.classList.add("hidden");
        overlay.classList.add("hidden");
    });
});
</script>
